  !$acc enter data create (YD%F_DIVDP)
  !$acc update device (YD%F_DIVDP)
  CALL COPY (YD%F_DIVDP, LDCREATED=.TRUE.)
  !$acc enter data attach (YD%F_DIVDP)
ENDIF

END SUBROUTINE


  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%STTEM (IL1(1):IU1(1)))
  READ (KLUN) YD%STTEM
ENDIF
READ (KLUN) LSTDEN
IF (LSTDEN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1

  READ (KLUN) YD%STDEN
ENDIF
READ (KLUN) LSTZ
IF (LSTZ) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%STZ (IL1(1):IU1(1)))
  READ (KLUN) YD%STZ
ENDIF
READ (KLUN) LSVETAH
IF (LSVETAH) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%SVETAH (IL1(1):IU1(1)))

SUBROUTINE LOAD_TCSGEOM_BLOCKED (KLUN, YD)
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCSGEOM_BLOCKED), INTENT (OUT), TARGET :: YD
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LRATATH, LRATATX, LRCOLON, LRINDX, LRINDY, LRSILON

READ (KLUN) LRCOLON
IF (LRCOLON) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RCOLON (IL2(1):IU2(1), IL2(2):IU2(2)))

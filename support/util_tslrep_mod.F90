SUBROUTINE SAVE_TSLREP (KLUN, YD)
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
CLASS (TSLREP), INTENT (IN), TARGET :: YD
LOGICAL :: LLADCORE, LNADCORE, LNADMAP, LRSASIGN

WRITE (KLUN) YD%NGPTOTAD
LNADMAP = ALLOCATED (YD%NADMAP)
WRITE (KLUN) LNADMAP
IF (LNADMAP) THEN
  WRITE (KLUN) LBOUND (YD%NADMAP)
  WRITE (KLUN) UBOUND (YD%NADMAP)
  WRITE (KLUN) YD%NADMAP
ENDIF
LNADCORE = ALLOCATED (YD%NADCORE)
WRITE (KLUN) LNADCORE
IF (LNADCORE) THEN
  WRITE (KLUN) LBOUND (YD%NADCORE)
  WRITE (KLUN) UBOUND (YD%NADCORE)
  WRITE (KLUN) YD%NADCORE
ENDIF
LLADCORE = ALLOCATED (YD%LADCORE)
WRITE (KLUN) LLADCORE
IF (LLADCORE) THEN
  WRITE (KLUN) LBOUND (YD%LADCORE)
  WRITE (KLUN) UBOUND (YD%LADCORE)
  WRITE (KLUN) YD%LADCORE
ENDIF
LRSASIGN = ALLOCATED (YD%RSASIGN)
WRITE (KLUN) LRSASIGN
IF (LRSASIGN) THEN
  WRITE (KLUN) LBOUND (YD%RSASIGN)
  WRITE (KLUN) UBOUND (YD%RSASIGN)
  WRITE (KLUN) YD%RSASIGN
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TSLREP (KLUN, YD)
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
CLASS (TSLREP), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LLADCORE, LNADCORE, LNADMAP, LRSASIGN

READ (KLUN) YD%NGPTOTAD
READ (KLUN) LNADMAP
IF (LNADMAP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NADMAP (IL1(1):IU1(1)))
  READ (KLUN) YD%NADMAP
ENDIF
READ (KLUN) LNADCORE
IF (LNADCORE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NADCORE (IL1(1):IU1(1)))
  READ (KLUN) YD%NADCORE
ENDIF
READ (KLUN) LLADCORE
IF (LLADCORE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%LADCORE (IL1(1):IU1(1)))
  READ (KLUN) YD%LADCORE
ENDIF
READ (KLUN) LRSASIGN
IF (LRSASIGN) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RSASIGN (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) YD%RSASIGN
ENDIF
END SUBROUTINE



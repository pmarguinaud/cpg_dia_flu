SUBROUTINE SAVE_TCOM (KLUN, YD)
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCOM), INTENT (IN), TARGET :: YD
LOGICAL :: LGTTLIN, LOMLDTH, LSSTMSK, LSSTPRE, LTRAFLX

WRITE (KLUN) YD%NVCOM
LOMLDTH = ALLOCATED (YD%OMLDTH)
WRITE (KLUN) LOMLDTH
IF (LOMLDTH) THEN
  WRITE (KLUN) LBOUND (YD%OMLDTH)
  WRITE (KLUN) UBOUND (YD%OMLDTH)
  WRITE (KLUN) YD%OMLDTH
ENDIF
LGTTLIN = ALLOCATED (YD%GTTLIN)
WRITE (KLUN) LGTTLIN
IF (LGTTLIN) THEN
  WRITE (KLUN) LBOUND (YD%GTTLIN)
  WRITE (KLUN) UBOUND (YD%GTTLIN)
  WRITE (KLUN) YD%GTTLIN
ENDIF
LSSTPRE = ALLOCATED (YD%SSTPRE)
WRITE (KLUN) LSSTPRE
IF (LSSTPRE) THEN
  WRITE (KLUN) LBOUND (YD%SSTPRE)
  WRITE (KLUN) UBOUND (YD%SSTPRE)
  WRITE (KLUN) YD%SSTPRE
ENDIF
LSSTMSK = ALLOCATED (YD%SSTMSK)
WRITE (KLUN) LSSTMSK
IF (LSSTMSK) THEN
  WRITE (KLUN) LBOUND (YD%SSTMSK)
  WRITE (KLUN) UBOUND (YD%SSTMSK)
  WRITE (KLUN) YD%SSTMSK
ENDIF
WRITE (KLUN) YD%LOMLDTH
LTRAFLX = ALLOCATED (YD%TRAFLX)
WRITE (KLUN) LTRAFLX
IF (LTRAFLX) THEN
  WRITE (KLUN) LBOUND (YD%TRAFLX)
  WRITE (KLUN) UBOUND (YD%TRAFLX)
  WRITE (KLUN) YD%TRAFLX
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TCOM (KLUN, YD)
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCOM), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LGTTLIN, LOMLDTH, LSSTMSK, LSSTPRE, LTRAFLX

READ (KLUN) YD%NVCOM
READ (KLUN) LOMLDTH
IF (LOMLDTH) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%OMLDTH (IL1(1):IU1(1)))
  READ (KLUN) YD%OMLDTH
ENDIF
READ (KLUN) LGTTLIN
IF (LGTTLIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%GTTLIN (IL1(1):IU1(1)))
  READ (KLUN) YD%GTTLIN
ENDIF
READ (KLUN) LSSTPRE
IF (LSSTPRE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%SSTPRE (IL1(1):IU1(1)))
  READ (KLUN) YD%SSTPRE
ENDIF
READ (KLUN) LSSTMSK
IF (LSSTMSK) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%SSTMSK (IL1(1):IU1(1)))
  READ (KLUN) YD%SSTMSK
ENDIF
READ (KLUN) YD%LOMLDTH
READ (KLUN) LTRAFLX
IF (LTRAFLX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%TRAFLX (IL1(1):IU1(1)))
  READ (KLUN) YD%TRAFLX
ENDIF
END SUBROUTINE



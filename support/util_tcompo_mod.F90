READ (KLUN) YD%LGHG_ACCO2
READ (KLUN) YD%LCO2SFC
READ (KLUN) YD%LCH4SFC
READ (KLUN) YD%LAEROSFC
READ (KLUN) YD%LFIRE
READ (KLUN) YD%LINJ
READ (KLUN) YD%LINJ_CHEM
READ (KLUN) YD%LINJ_GHG
READ (KLUN) YD%LCOMPO_DCVOC
READ (KLUN) YD%LCOMPO_DCANT
READ (KLUN) YD%LCOMPO_DCBB
READ (KLUN) YD%LCOMPO_DCDD
READ (KLUN) YD%LVOLC_ALTI
READ (KLUN) YD%LANT_HIGH
READ (KLUN) YD%LAERNITRATE
READ (KLUN) YD%LAERRESUSPENSION
READ (KLUN) YD%LAERSOA

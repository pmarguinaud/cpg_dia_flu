  READ (KLUN) YD%NASM0G
ENDIF
READ (KLUN) LNVALUE
IF (LNVALUE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NVALUE (IL1(1):IU1(1)))
  READ (KLUN) YD%NVALUE
ENDIF
READ (KLUN) LMYMS
IF (LMYMS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%MYMS (IL1(1):IU1(1)))
  READ (KLUN) YD%MYMS
ENDIF
READ (KLUN) LNSPZERO
IF (LNSPZERO) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NSPZERO (IL1(1):IU1(1)))
  READ (KLUN) YD%NSPZERO
ENDIF
READ (KLUN) LNSE0L
IF (LNSE0L) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NSE0L (IL1(1):IU1(1)))
  READ (KLUN) YD%NSE0L
ENDIF
READ (KLUN) LRLAPDI
IF (LRLAPDI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLAPDI (IL1(1):IU1(1)))
  READ (KLUN) YD%RLAPDI
ENDIF
READ (KLUN) LRLAPIN
IF (LRLAPIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLAPIN (IL1(1):IU1(1)))
  READ (KLUN) YD%RLAPIN
ENDIF
END SUBROUTINE





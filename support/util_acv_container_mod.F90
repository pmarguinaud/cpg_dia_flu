SUBROUTINE SAVE_ACV_CONTAINER (KLUN, YD)
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
CLASS (ACV_CONTAINER), INTENT (IN), TARGET :: YD
LOGICAL :: LRACV1D, LRGPACV2D, LRGPACV3D, LRSPACV2D, LRSPACV3D

LRACV1D = ALLOCATED (YD%RACV1D)
WRITE (KLUN) LRACV1D
IF (LRACV1D) THEN
  WRITE (KLUN) LBOUND (YD%RACV1D)
  WRITE (KLUN) UBOUND (YD%RACV1D)
  WRITE (KLUN) YD%RACV1D
ENDIF
LRSPACV2D = ALLOCATED (YD%RSPACV2D)
WRITE (KLUN) LRSPACV2D
IF (LRSPACV2D) THEN
  WRITE (KLUN) LBOUND (YD%RSPACV2D)
  WRITE (KLUN) UBOUND (YD%RSPACV2D)
  WRITE (KLUN) YD%RSPACV2D
ENDIF
LRGPACV2D = ALLOCATED (YD%RGPACV2D)
WRITE (KLUN) LRGPACV2D
IF (LRGPACV2D) THEN
  WRITE (KLUN) LBOUND (YD%RGPACV2D)
  WRITE (KLUN) UBOUND (YD%RGPACV2D)
  WRITE (KLUN) YD%RGPACV2D
ENDIF
LRSPACV3D = ALLOCATED (YD%RSPACV3D)
WRITE (KLUN) LRSPACV3D
IF (LRSPACV3D) THEN
  WRITE (KLUN) LBOUND (YD%RSPACV3D)
  WRITE (KLUN) UBOUND (YD%RSPACV3D)
  WRITE (KLUN) YD%RSPACV3D
ENDIF
LRGPACV3D = ALLOCATED (YD%RGPACV3D)
WRITE (KLUN) LRGPACV3D
IF (LRGPACV3D) THEN
  WRITE (KLUN) LBOUND (YD%RGPACV3D)
  WRITE (KLUN) UBOUND (YD%RGPACV3D)
  WRITE (KLUN) YD%RGPACV3D
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_ACV_CONTAINER (KLUN, YD)
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
CLASS (ACV_CONTAINER), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2), IL3(3), IU3(3)
LOGICAL :: LRACV1D, LRGPACV2D, LRGPACV3D, LRSPACV2D, LRSPACV3D

READ (KLUN) LRACV1D
IF (LRACV1D) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RACV1D (IL1(1):IU1(1)))
  READ (KLUN) YD%RACV1D
ENDIF
READ (KLUN) LRSPACV2D
IF (LRSPACV2D) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RSPACV2D (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) YD%RSPACV2D
ENDIF
READ (KLUN) LRGPACV2D
IF (LRGPACV2D) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RGPACV2D (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) YD%RGPACV2D
ENDIF
READ (KLUN) LRSPACV3D
IF (LRSPACV3D) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%RSPACV3D (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  READ (KLUN) YD%RSPACV3D
ENDIF
READ (KLUN) LRGPACV3D
IF (LRGPACV3D) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%RGPACV3D (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  READ (KLUN) YD%RGPACV3D
ENDIF
END SUBROUTINE



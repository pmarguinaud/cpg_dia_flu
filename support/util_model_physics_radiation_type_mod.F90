INTERFACE SAVE
MODULE PROCEDURE SAVE_MODEL_PHYSICS_RADIATION_TYPE
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_MODEL_PHYSICS_RADIATION_TYPE
END INTERFACE

